const startVideoCall = async (req, res) => {
  try {
    const { appointmentId } = req.body;
    const docId = req.docId;

    console.log("üì• Received appointmentId from body:", appointmentId);
    console.log("ü™™ docId from token:", docId);

    // Find appointment
    const appointment = await appointmentModel.findOne({ _id: appointmentId });
    console.log("üîç Found appointment:", appointment);

    if (!appointment) {
      return res.json({ success: false, message: "Appointment not found" });
    }

    console.log("ü©∫ docId in appointment:", appointment.docId?.toString());

    if (appointment.docId?.toString() !== docId) {
      return res.json({
        success: false,
        message: "Unauthorized: docId mismatch",
      });
    }

    appointment.callStarted = true;
    await appointment.save();

    // Notify patient via Socket.IO
    req.io
      .to(appointment.userId.toString())
      .emit("call-started", { appointmentId });

    res.json({ success: true, message: "Call started successfully" });
  } catch (error) {
    console.log("‚ùå Error in startVideoCall:", error);
    res.json({ success: false, message: error.message });
  }
};

